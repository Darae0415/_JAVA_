public class java {
  public static void main(String[] args) {
    /* 객체와 인스턴스 차이
     * 객체(Object) : 일반화 (ex : 사과)
     * 인스턴스(instance) : 구체화 (ex : 독이 든 사과)
     * 
     * 
     * 객체 선언 : String(데이터타입) num(변수명);
     * 객체 생성 : 클래스명 변수명 : new 클래스명(메소드나 생성자등);
     * 
     * 클래스 접근방식
     * private   : 같은 클래스 내에서만 접근가능
     * defalut   : 같은 패키지 내애서만 접근가능
     * protected : 같은 패키지, 그리고 패키지의 자손 클래스에서 접근가능
     * public    : 접근 제한이 없음
     * 
     * 사용가능한 접근 제어자
     * class : public , default
     * 메소드 & 멤버변수 : public , protected , default , public
     * 지역변수는 사용하지 않음.
    */
  }
}
